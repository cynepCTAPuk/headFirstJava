package puzzle_11;

public class LastLaugh {
    public static void main(String args[]) {
        System.out.print("H" + "a");
        System.out.print('H' + 'a');
        System.out.println();
        System.out.println(Integer.toBinaryString('H') + " " + (int) 'H');
        System.out.println(Integer.toBinaryString('a') + " " + (int) 'a');
        System.out.println(72 + 97);
    }
}
