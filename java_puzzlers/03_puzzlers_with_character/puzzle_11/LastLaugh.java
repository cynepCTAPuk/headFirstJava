package puzzle_11;

public class LastLaugh {
    public static void main(String args[]) {
        System.out.println("H" + "a"); // Ha
        System.out.println('H' + 'a'); // (int)'H' + (int)'a'

        System.out.println("" + 'H' + 'a');
        System.out.println("2 + 2 = " + 2 + 2);
        System.out.printf("%c%c",'H','a');
    }
}
